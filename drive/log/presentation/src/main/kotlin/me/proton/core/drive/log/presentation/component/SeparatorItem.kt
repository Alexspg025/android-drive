    title: String,
    modifier: Modifier = Modifier,
) {
    Text(
        text = title,
        textAlign = TextAlign.Center,
        style = ProtonTheme.typography.captionStrongNorm,
        modifier = modifier
            .fillMaxWidth()
            .background(color = ProtonTheme.colors.backgroundSecondary)
            .padding(horizontal = SmallSpacing, vertical = ExtraSmallSpacing),
    )
}
